import type { EngineDef, ComponentDef, FieldDef } from '../MetaTypes';

function _primitiveJsType(t: string): string {
  switch (t) {
    case 'number': case 'slider': return 'number';
    case 'boolean': return 'boolean';
    case 'string': return 'string';
    case 'vector3': case 'euler': return '{x:number,y:number,z:number}';
    case 'vector2': return '{x:number,y:number}';
    case 'color': return '{r:number,g:number,b:number}';
    case 'asset': return 'string|null';
    default: return '*';
  }
}

function fieldToJsDocType(f: FieldDef): string {
  switch (f.type) {
    case 'number':
    case 'slider':   return 'number';
    case 'boolean':  return 'boolean';
    case 'string':   return 'string';
    case 'vector3':
    case 'euler':    return '{x:number,y:number,z:number}';
    case 'vector2':  return '{x:number,y:number}';
    case 'color':    return '{r:number,g:number,b:number}';
    case 'select':
      if (f.options?.length) {
        return f.options.map(o => `'${o.value}'`).join('|');
      }
      return 'string';
    case 'asset':    return 'string|null';
    case 'array':
      if (f.tupleTypes?.length) return `[${f.tupleTypes.map(_primitiveJsType).join(', ')}]`;
      return f.itemType ? `${_primitiveJsType(f.itemType)}[]` : 'Array';
    case 'union':
      return f.unionTypes?.length
        ? f.unionTypes.map(_primitiveJsType).join('|')
        : '*';
    default:         return '*';
  }
}

function emitTypedef(c: ComponentDef): string {
  const isScript = c.category === 'Scripts';
  const className = isScript ? c.typeId.replace(/^Script:/, '') : c.typeId;

  const fieldProps = c.fields.map(f => {
    const jsType = fieldToJsDocType(f);
    return `${f.key}: ${jsType}`;
  });

  const compDesc = c.description ? ` — ${c.description}` : '';

  if (isScript) {
    // Script classes: @typedef {FluxionBehaviour & { speed: number }} PlayerController
    const intersection = fieldProps.length
      ? `FluxionBehaviour & { ${fieldProps.join(', ')} }`
      : 'FluxionBehaviour';
    const lines: string[] = [`/**`];
    if (c.deprecated) lines.push(` * @deprecated`);
    lines.push(
      ` * @typedef {${intersection}} ${className}`,
      ` * @description Script: ${c.displayName}${compDesc}`,
      ` */`,
    );
    return lines.join('\n');
  }

  // Normal component
  const lines: string[] = [
    `/**`,
  ];
  if (c.deprecated) lines.push(` * @deprecated`);
  lines.push(
    ` * @typedef {Object} ${className}`,
    ` * @description [${c.category}] ${c.displayName} component${compDesc}`,
    ` * @property {boolean} enabled Whether this component is active`,
  );
  for (const f of c.fields) {
    const jsType = fieldToJsDocType(f);
    const label  = f.label !== f.key ? f.label : f.key;
    const fieldDesc = f.description ? ` — ${f.description}` : '';
    if (f.readOnly) lines.push(` * @readonly`);
    lines.push(` * @property {${jsType}} ${f.optional ? `[${f.key}]` : f.key} ${label}${fieldDesc}`);
  }
  lines.push(` */`);
  return lines.join('\n');
}

