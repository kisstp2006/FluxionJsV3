import type { EngineDef, ComponentDef, FieldDef } from '../MetaTypes';

// ── Type mapping ─────────────────────────────────────────────

function _primitiveToTsType(t: string): string {
  switch (t) {
    case 'number': case 'slider': return 'number';
    case 'boolean': return 'boolean';
    case 'string': return 'string';
    case 'vector3': case 'euler': return 'Vector3';
    case 'vector2': return 'Vector2';
    case 'color': return 'Color';
    case 'asset': return 'string | null';
    default: return 'unknown';
  }
}

function fieldToTsType(f: FieldDef): string {
  switch (f.type) {
    case 'number':
    case 'slider':
      return 'number';
    case 'boolean':
      return 'boolean';
    case 'string':
      return 'string';
    case 'vector3':
    case 'euler':
      return 'Vector3';
    case 'vector2':
      return 'Vector2';
    case 'color':
      return 'Color';
    case 'select':
      if (f.options?.length) {
        return f.options.map(o => `'${o.value}'`).join(' | ');
      }
      return 'string';
    case 'asset':
      return 'string | null';
    case 'array':
      if (f.tupleTypes?.length) {
        return `[${f.tupleTypes.map(_primitiveToTsType).join(', ')}]`;
      }
      return f.itemType ? `${_primitiveToTsType(f.itemType)}[]` : 'unknown[]';
    case 'union':
      return f.unionTypes?.length
        ? f.unionTypes.map(_primitiveToTsType).join(' | ')
        : 'unknown';
    default:
      return 'unknown';
  }
}

function emitField(f: FieldDef, indent = '    '): string {
  const tsType = fieldToTsType(f);
  const opt    = f.optional ? '?' : '';
  const rdOnly = f.readOnly ? 'readonly ' : '';
  const range  = f.min !== undefined && f.max !== undefined
    ? `range ${f.min}–${f.max}`
    : f.min !== undefined ? `min ${f.min}`
    : f.max !== undefined ? `max ${f.max}` : '';
  const parts = [f.label !== f.key ? f.label : '', range, f.description].filter(Boolean);
  const doc = parts.join(', ');
  const comment = doc ? `${indent}/** ${doc} */\n` : '';
  return `${comment}${indent}${rdOnly}${f.key}${opt}: ${tsType};`;
}

function emitComponent(c: ComponentDef): string {
  const isScript = c.category === 'Scripts';
  const deprecatedTag = c.deprecated ? ` @deprecated` : '';
  const descSuffix = c.description ? ` — ${c.description}` : '';

  if (isScript) {
    // Script classes extend FluxionBehaviour — no enabled field (that's ScriptComponent's)
    const lines: string[] = [
      `  /**${deprecatedTag} Script: ${c.displayName}${descSuffix} */`,
      `  interface ${c.typeId.replace(/^Script:/, '')} extends FluxionBehaviour {`,
    ];
    for (const f of c.fields) {
      lines.push(emitField(f));
    }
    lines.push(`  }`);
    return lines.join('\n');
  }

  const lines: string[] = [
    `  /**${deprecatedTag} [${c.category}] ${c.displayName} component${descSuffix} */`,
    `  interface ${c.typeId} {`,
    `    /** Whether this component is active */`,
    `    enabled: boolean;`,
  ];
  for (const f of c.fields) {
    lines.push(emitField(f));
  }
  lines.push(`  }`);
  return lines.join('\n');
}

