// ── Component map emitter ─────────────────────────────────────

function emitComponentMap(def: EngineDef): string {
  const lines: string[] = [
    '',
    '  // ── Component map (typeId → interface) ─────────────────',
    '  /**',
    '   * Maps every component type ID to its concrete interface.',
    '   * Enables typed component access:',
    '   *   const rb = this.getComponent(\'Rigidbody\'); // → Rigidbody | null',
    '   */',
    '  interface ComponentMap {',
  ];
  for (const c of def.components) {
    if (c.category === 'Scripts') continue;
    lines.push(`    ${JSON.stringify(c.typeId)}: ${c.typeId};`);
  }
  lines.push('  }');
  lines.push('  /** Union of all registered component type IDs */');
  lines.push('  type ComponentTypeId = keyof ComponentMap;');
  return lines.join('\n');
}

// ── Main export ──────────────────────────────────────────────

export class TypeScriptGenerator {
  static generate(def: EngineDef): string {
    const sections: string[] = [STATIC_PREAMBLE_HEAD];

    // Group components by category
    const byCategory = new Map<string, ComponentDef[]>();
    for (const c of def.components) {
      const cat = c.category;
      const arr = byCategory.get(cat) ?? [];
      arr.push(c);
      byCategory.set(cat, arr);
    }

    for (const [cat, comps] of byCategory) {
      sections.push(`\n  // ── ${cat} ` + '─'.repeat(Math.max(2, 50 - cat.length)));
      for (const c of comps) {
        sections.push(emitComponent(c));
      }
    }

    sections.push(emitComponentMap(def));
    sections.push(STATIC_BEHAVIOUR);
    sections.push(STATIC_SUFFIX);
    sections.push(STATIC_UTIL_FUNCTIONS);
    return sections.join('\n');
  }
}
